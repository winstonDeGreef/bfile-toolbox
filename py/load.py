from decimal import Decimal, getcontext
import requests
from re import sub
# Includes decimal point and may include whitespace, you might get this file from: https://newton.ex.ac.uk/research/qsystems/collabs/pi/pi4.txt
pi = sub(r'\s+', '', requests.get("https://newton.ex.ac.uk/research/qsystems/collabs/pi/pi4.txt").text)
getcontext().prec = len(pi)+5
def overlap_with_pi(n, d):
    i, s = 0, format(Decimal(n)/Decimal(d), "f")
    while s[i] == pi[i] and i < len(s): i += 1
    return i-1

